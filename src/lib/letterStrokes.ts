export function strokesFor(ch: string): Stroke[] {
  if (LETTER_STROKES[ch]) return LETTER_STROKES[ch];
  const upper = ch.toUpperCase();
  if (LETTER_STROKES[upper]) return LETTER_STROKES[upper];
  return [[[0.15, 0.5], [0.85, 0.5]]]; // fallback: small dash for unmapped glyphs
}
